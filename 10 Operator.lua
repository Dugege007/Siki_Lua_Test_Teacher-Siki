-- 数学运算符 + - * / % ^
-- 和其他编程语言中的用法差不多

a = 10.0
b = 3

-- 加
print(a + b)
-- 减
print(a - b)
-- 乘
print(a * b)
-- 除
print(a / b)
-- 取余
print(a % b)
-- 幂
print(a ^ b)

-- 关系运算符 == ~= >= <= > <
-- 判断结果为 true 或 false

if(a == b) then
	print("a b 相等")
else
	print("a b 不相等")
end

